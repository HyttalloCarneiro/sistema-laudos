def extract_text_from_pdf(pdf_file):
    """Extrai texto de um arquivo PDF"""
    try:
        pdf_reader = PyPDF2.PdfReader(pdf_file)
        text = ""
        
        # Extrair texto de todas as p√°ginas
        for page in pdf_reader.pages:
            text += page.extract_text() + "\n"
        
        return text
    except Exception as e:
        st.error(f"‚ùå Erro ao extrair texto do PDF: {str(e)}")
        return None

def extract_process_data(text):
    """Extrai dados do processo a partir do texto do PDF"""
    if not text:
        return None
    
    # Padr√µes de regex para extrair informa√ß√µes
    patterns = {
        'numero_processo': [
            r'(?:Processo|PROCESSO|N¬∫|N¬∞|N√∫mero)[\s\.:]*(\d{7}-\d{2}\.\d{4}\.\d\.\d{2}\.\d{4})',
            r'(\d{7}-\d{2}\.\d{4}\.\d\.\d{2}\.\d{4})',
            r'(?:Autos|AUTOS)[\s\.:]*(\d{7}-\d{2}\.\d{4}\.\d\.\d{2}\.\d{4})',
            r'(\d{4}\.\d{2}\.\d{2}\.\d{6}-\d)',
            r'(\d{4}\.\d{2}\.\d{2}\.\d{4}-\d{2})'
        ],
        'nome_parte': [
            r'(?:Autor|AUTOR|Requerente|REQUERENTE|Parte|PARTE)[\s\.:]*([A-Z√Å√Ä√Ç√É√â√à√ä√ç√å√é√ì√í√î√ï√ö√ô√õ√á][a-z√°√†√¢√£√©√®√™√≠√¨√Æ√≥√≤√¥√µ√∫√π√ª√ß\s]+)',
            r'(?:Nome|NOME)[\s\.:]*([A-Z√Å√Ä√Ç√É√â√à√ä√ç√å√é√ì√í√î√ï√ö√ô√õ√á][a-z√°√†√¢√£√©√®√™√≠√¨√Æ√≥√≤√¥√µ√∫√π√ª√ß\s]+)',
            r'(?:Periciando|PERICIANDO)[\s\.:]*([A-Z√Å√Ä√Ç√É√â√à√ä√ç√å√é√ì√í√î√ï√ö√ô√õ√á][a-z√°√†√¢√£√©√®√™√≠√¨√Æ√≥√≤√¥√µ√∫√π√ª√ß\s]+)'
        ]
    }
    
    extracted_data = {}
    
    # Extrair n√∫mero do processo
    for pattern in patterns['numero_processo']:
        match = re.search(pattern, text, re.IGNORECASE)
        if match:
            extracted_data['numero_processo'] = match.group(1)
            break
    
    # Extrair nome da parte
    for pattern in patterns['nome_parte']:
        match = re.search(pattern, text, re.IGNORECASE)
        if match:
            nome = match.group(1).strip()
            # Limpar o nome (remover quebras de linha e espa√ßos extras)
            nome = re.sub(r'\s+', ' ', nome)
            # Limitar o tamanho do nome
            if len(nome) > 50:
                nome = nome[:50] + "..."
            extracted_data['nome_parte'] = nome
            break
    
    # Tentar determinar o tipo de per√≠cia baseado no conte√∫do
    tipo_pericia = "Aux√≠lio Doen√ßa (AD)"  # Padr√£o
    
    if re.search(r'aux√≠lio.?acidente|acidente.?trabalho', text, re.IGNORECASE):
        tipo_pericia = "Aux√≠lio Acidente (AA)"
    elif re.search(r'bpc|benef√≠cio.?presta√ß√£o.?continuada|loas', text, re.IGNORECASE):
        tipo_pericia = "Benef√≠cio de Presta√ß√£o Continuada (BPC)"
    elif re.search(r'dpvat|seguro.?obrigat√≥rio', text, re.IGNORECASE):
        tipo_pericia = "Seguro DPVAT (DPVAT)"
    elif re.search(r'medica√ß√£o|medicamento|fornecimento', text, re.IGNORECASE):
        tipo_pericia = "Fornecimento de medica√ß√£o (MED)"
    elif re.search(r'imposto.?renda|ir|dedu√ß√£o', text, re.IGNORECASE):
        tipo_pericia = "Imposto de renda (IR)"
    elif re.search(r'interdi√ß√£o|curatela|incapacidade', text, re.IGNORECASE):
        tipo_pericia = "Interdi√ß√£o (INT)"
    elif re.search(r'erro.?m√©dico|responsabilidade.?m√©dica|dano.?m√©dico', text, re.IGNORECASE):
        tipo_pericia = "Erro m√©dico (ERRO)"
    
    extracted_data['tipo_pericia'] = tipo_pericia
    
    return extracted_data

def format_date_br(date_str):
    """Converte data de YYYY-MM-DD para DD-MM-YYYY"""
    if isinstance(date_str, str) and len(date_str) == 10:
        year, month, day = date_str.split('-')
        return f"{day}-{month}-{year}"
    return date_str

def format_date_iso(date_str):
    """Converte data de DD-MM-YYYY para YYYY-MM-DD"""
    if isinstance(date_str, str) and len(date_str) == 10 and '-' in date_str:
        parts = date_str.split('-')
        if len(parts) == 3:
            if len(parts[0]) == 2:
                return f"{parts[2]}-{parts[1]}-{parts[0]}"
            else:
                return date_str
    return date_str

